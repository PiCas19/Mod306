    private void openButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openButtonActionPerformed
        JFileChooser open = new JFileChooser();
        File directory = new File("Snowflake template");
        if(directory.exists()){
            open.setCurrentDirectory(directory);
            open.showDialog(null, "Open");
            BufferedReader br = null;
            try {
                File file = open.getSelectedFile();
                if(!file.getAbsolutePath().matches("[^template_][.csv$]")){
                    br = new BufferedReader ( new FileReader (file));
                    List<String[]> list = new ArrayList<>();
                    String line = "";
                    while((line = br.readLine()) != null){
                        String[] array = line.split("; ");
                        list.add(array);
                    }
            
                    int npoints = 0;
                    int[] xpoints;
                    int[] ypoints;

                    int index = 0;

                    for(String[] lines : list){
                        npoints = Integer.parseInt(lines[0]);
                        xpoints = new int[npoints];
                        ypoints = new int[npoints];
                        String x =  lines[1].substring(1, lines[1].length()-1);
                        String y =  lines[2].substring(1, lines[2].length()-1);
                        String[] xs = x.split(" ");
                        String[] ys = y.split(" ");
                        int count = 0;
                        for(int i = 0; i < npoints; i++){
                            xpoints[i] = Integer.parseInt(
                                    xs[i].replace(",", "")
                            );
                            ypoints[i] = Integer.parseInt(
                                    ys[i].replace(",", "")
                            );
                        }
                        ++index;
                        Polygon p = new Polygon(xpoints, ypoints, npoints);
                        this.polygons.add(p);
                    }

                    this.snowflakeFrame = new SnowflakeFrame();
                    this.snowflakeFrame.setPolygons(polygons);
                    this.snowflakeFrame.setSize(1024, 768);
                    this.snowflakeFrame.setMinimumSize(new Dimension(1024, 768));
                    this.snowflakeFrame.setVisible(true);
                }   

            } catch (FileNotFoundException ex) {

            } catch (IOException e) {
            }   
            catch(Exception ex){

            }
        }
    }//GEN-LAST:event_openButtonActionPerformed

